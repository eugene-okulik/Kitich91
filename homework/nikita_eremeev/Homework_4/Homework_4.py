my_dict = {
    'tuple': (1, 2, 3, 4, 5, 6),
    'list': [10, 20, 30, 40, 50],
    'dict': {'a': 1, 'b': 2, 'c': 3, 'd': 4, 'e': 5},
    'set': {100, 200, 300, 400, 500}
}

# 1. Для tuple - вывести последний элемент
print("Последний элемент tuple:", my_dict['tuple'][-1])

# 2. Для list - добавить в конец и удалить второй элемент
my_dict['list'].append(60)  # добавили в конец
del my_dict['list'][1]      # удалили второй элемент (индекс 1)

# 3. Для dict - добавить ключ-кортеж и удалить элемент
# Важно: ('i am a tuple',) - это кортеж из одного элемента!
my_dict['dict'][('i am a tuple',)] = "любое значение"
del my_dict['dict']['a']    # удаляем элемент с ключом 'a'

# 4. Для set - добавить и удалить элемент
my_dict['set'].add(600)           # добавляем элемент
my_dict['set'].discard(100)       # удаляем элемент (discard не выдаст ошибку, если элемента нет)

# Выводим весь словарь
print("\nИтоговый словарь:")
print(my_dict)
